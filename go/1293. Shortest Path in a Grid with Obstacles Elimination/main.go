func shortestPath(grid [][]int, k int) int {
	var (
		visit [][][]bool
		n     = len(grid)
		m     = len(grid[0])
	)
	for range grid {
		var row [][]bool
		for j := 0; j < m; j++ {
			cell := make([]bool, k)
			row = append(row, cell)
		}
		visit = append(visit, row)
	}
	var (
		queue = [][]int{{0, 0, 0}}
		dirs  = [][]int{
			{-1, 0},
			{1, 0},
			{0, 1},
			{0, -1},
		}
		step = 0
	)

	visit[0][0][0] = true

	for len(queue) > 0 {
		size := len(queue)
		for l := 0; l < size; l++ {
			item := queue[l]
			i, j, currK := item[0], item[1], item[2]
			if i == n-1 && j == m-1 {
				return step
			}
			for _, d := range dirs {
				newI := i + d[0]
				newJ := j + d[1]
				newK := currK
				if newI >= 0 && newI < n && newJ >= 0 && newJ < m {
					if grid[newI][newJ] == 1 {
						newK++
					}
					if newK <= k && !visit[newI][newJ][newK] {
						visit[newI][newJ][newK] = true
						queue = append(queue, []int{newI, newJ, newK})
					}
				}
			}
		}
		queue = queue[size:]
		step += 1
	}
	return -1
}
